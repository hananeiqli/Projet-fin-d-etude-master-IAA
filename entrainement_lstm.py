def create_model(learning_rate):

    model = Sequential()

    model.add(
        LSTM(
            units=64,
            input_shape=(
                X_train.shape[1],
                X_train.shape[2]
            )
        )
    )

    model.add(
        Dense(
            32,
            activation="relu"
        )
    )

    model.add(Dense(1))

    model.compile(
        optimizer=Adam(
            learning_rate=learning_rate
        ),
        loss="mse"
    )

    return model

