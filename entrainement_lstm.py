def fetch_metric(metric_name):

    params = {
        "query": metric_name,
        "start": start,
        "end": end,
        "step": step
    }

    response = requests.get(
        url,
        params=params,
        timeout=20
    )

    response.raise_for_status()

    data = response.json()

    if data["status"] != "success":
        raise Exception(
            f"Erreur Prometheus : {metric_name}"
        )

    return data["data"]["result"]

